		<div id="collapse-DLVRD-Twelve" class="collapse" aria-labelledby="heading-DLVRD-Twelve" data-parent="#accordion-DLVRD">
			<div class="card-body">
				Dry ice is the name given to carbon dioxide when it is in a solid state. Dry ice itself is not poisonous, but the surface of the solid is so cold that it should not be handled without gloves. For safety purposes, recipients should follow the guidelines below:
				<ul>
					<li>Do not touch dry ice to skin, mouth or eyes.</li>
					<li>Do not put dry ice in drinks or eat it.</li>
					<li>Keep dry ice out of reach of children and pets.</li>
					<li>Store dry ice in an open and well-ventilated area.</li>
					<li>Never pick up dry ice with bare hands. Always wear hand protection: an oven mitt or heavy cloth towel works well.</li>
					<li>To dispose of dry ice, use a towel or mitt to protect hands. Place in a well-ventilated area like a garage or shop, or outdoors on a porch or patio, and out of the reach of children or pets. Within 24 hours the solid dry ice will convert to gas. Do not breathe carbon dioxide gas deeply or for an extended period of time</li></ul>
