				A: PlatePoints Dinner Dollars can be applied to your order at checkout. Use them towards sides and sweets freezer items, assembly or service fees or dinners for your family.
				<p>Dinner Dollars must be used in conjunction with a standard order or can be applied on a second order in the same menu month for any items you choose. They are not intended to be used during a freezer sale or in conjunction with other discounts. Dinner Dollars can only be redeemed at participating stores.</p>
				<p><strong>Q: Do I have to use my Dinner Dollars at my next visit? </strong><br />
					A: No, you don't have to use your Dinner Dollars during your next visit, but, don't wait too long. Dinner Dollars expire 45 days from their issue date.</p>
				<p><strong>Q: How soon will the Dinner Dollars appear in my account? </strong><br />
					A: Your Dinner Dollars will appear once you reach a 200 PlatePoint interval. </p>
				<p><strong>Q: Can I give my Dinner Dollars to someone else?</strong><br />
					A: Dinner Dollars are non-transferable.</p>
				<p><strong>Q: Do Dinner Dollars expire? How will I know when my Dinner Dollars expire, will you notify me? </strong><br />
					A: Yes, Dinner Dollars expire 45 days from their issue date. You can always check your account for expiration dates on your available Dinner Dollars.  We will also send you a monthly email to keep you posted about your available Dinner Dollars and when they expire. </p>
				<p><strong>Q: Do my purchases using Dinner Dollars count for PlatePoints?</strong><br />
					A: You get PlatePoints for money spent not Dinner Dollars used.</p>
				<p><strong>Q: Can I redeem my Dinner Dollars when I sign up in-store? </strong><br />
					A: You may use the Dinner Dollars already appearing in your account in-store on orders over the standard order, for sides and sweets to round out your plate, or service fees. Dinner Dollars for orders placed that day will become available to you 7 days after your session and can be used on a future order. </p>
				<p><strong>Q: Can I use my Dinner Dollars in conjunction with other promotions or coupons available?</strong><br />
					A: No, Dinner Dollars cannot be used in conjunction with other promotions or coupons.</p>
