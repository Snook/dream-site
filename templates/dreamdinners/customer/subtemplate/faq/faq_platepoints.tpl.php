				<p><strong>Q: What is PlatePoints? </strong><br />
					A: PlatePoints is Dream Dinners' guest loyalty program where we reward you for serving homemade dinners to your family. You will earn 1 PlatePoint for every dollar you spend. For every 200 PlatePoints you accrue, you'll receive 5 Dinner Dollars.</p>
				<p><strong>Q: How do I earn PlatePoints?</strong><br />
					A: You earn PlatePoints based on your Dream Dinners activity. You earn 1 PlatePoint for every dollar you spend. You also earn PlatePoints for bonus activities including:</p>
				<ul>
					<li>Double points when you sign up for your next qualifying order before or during your current month's visit</li>
					<li>Invite Friends - get 10 Dinner Dollars for each new friend you refer to Dream Dinners that completes an order</li>
					<li>Rate your dinners in My Meals on DreamDinners.com - earn 5 PlatePoints per qualifying meal you rate</li>
				</ul>
				<p><strong>Q: What happens if I don't place my order before or during a visit?</strong><br />
					A: You still earn PlatePoints! You will earn 1 PlatePoint for every dollar you spend on your order, but you will not earn bonus points. </p>
				<p><strong>Q: When do I get my PlatePoints for my orders?</strong><br />
					A: PlatePoints appear in your account 7 days after your visit. PlatePoints for bonus activities, like rating your dinners in My Meals, appear immediately after you complete the activity. Dinner Dollars for friend referrals are awarded 7 days after your friends visit.
				</p>
				<p><strong>Q: What are my PlatePoints worth? </strong><br />
					A: You receive 5 Dinner Dollars for every 200 PlatePoints you earn.<br /></p>
				<p><strong>Q: What is the difference between PlatePoints and Dinner Dollars?</strong><br />
					You are awarded PlatePoints for every dollar you spend plus extra PlatePoints for bonus activities. PlatePoints never expire.</p>
				<p>Dinner Dollars, like a voucher, are made to be spent. You are awarded 5 Dinner Dollars for every 200 PlatePoints you earn. Your Dinner Dollars expire 45 days from their issue date. Birthday Dinner Dollars expire in your birth month. Simply apply them to an order within the 45-day window to redeem.</p>
				<p><strong>Q: Is there a limit on the PlatePoints I can earn per day/month/year?</strong><br />
					A: There is no limit to the PlatePoints you can earn.<br />
				</p>
				<p><strong>Q: What are pending  points?</strong><br />
					A: These PlatePoints are "pending" until 7 days after your visit, or your friend's visit has taken place. This ensures that any change in the order (e.g. additional items ordered, a cancellation or reschedule) is accounted for and your PlatePoints are calculated correctly.</p>
