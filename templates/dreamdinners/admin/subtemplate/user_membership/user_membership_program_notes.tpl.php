<p class="font-weight-bold">Meal Prep+ Notes</p>
<ul>
	<li>Once a membership is purchased with a Starting Menu month it cannot be changed. You will have to refund the membership and then purchase another one to changed the start month.</li>
	<li>Any future orders currently in their account that qualify for the new membership may need to be recalculated with the discount. Simply "Edit" the order.</li>
	<li>Discounts only apply to food on an order.</li>
	<li>A membership guest must have a qualifying consecutive standard order in the system for a menu month in order to place additional orders out of the freezer.</li>
	<li>A membership will be canceled if they do not place a standard consecutive order.</li>
	<li>The guest will be sent an email if their membership is canceled.</li>
	<li>The store has the option to allow for 1 skipped month. You must manually click the skip button to activate the skip and prevent a cancellation.</li>
	<li>Guests will have the ability to renew a membership within 30 days of their current membership ending.</li>
</ul>