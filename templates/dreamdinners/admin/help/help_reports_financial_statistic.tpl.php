<span class='Readme_header_large'>
Summary:
</span>

<br/>
The function of this report is to find all given orders that have occurred against sessions that happen during the currently selected time span.
For example, first "Select a month and enter a year" radio button and enter a month of August and a year of 2007. Once this is complete, then push
 the "Run Report" button. The interface will then show all orders that have been placed during sessions that have occurred in the month of August.
 The data is then grouped by individual sessions. Selecting a "plus" button will reveal individual session data. The "minus" button will close a currently open session.
<br/><br/>
<table width=100% class='Readme_title'><tr><td>Field Definitions:</td></tr></table>
<br/>
<span class='Readme_header'>Entrees Ordered (no promos)
</span>
<br/>
Total number of dinners ordered for that session not including promos (bonus dinners) being given away. This includes Sides and Fast Lane products.
<br/><br/>

<span class='Readme_header'>Entr&eacute;es Ordered Value (no promos)
</span>
<br/>
Total price of dinners ordered for that session not including promos (bonus dinners) being given away. This includes Sides and Fast Lane products.
<br/><br/>


<span class='Readme_header'>Promotional Items</span>
<br/>
The number of bonus dinners being given away for this session, when entered in the system using a promotional code.

<br/><br/>
<span class='Readme_header'>Total Entrees Ordered</span>
<br/>
Total of dinners ordered and paid for plus total of bonus dinners.

<br/><br/>
<span class='Readme_header'>Sides Ordered</span>
<br/>
Total number of dinners ordered from the Sides category.



<br/><br/>
<span class='Readme_header'>Fast Lane Items Ordered</span>
<br/>
Total number of dinners ordered from the Fast Lane category.

<br/><br/>
<span class='Readme_header'>Accessories Ordered</span>
<br/>
Reserved for future use of allowing the purchase of non-food items through the Dream Dinners web site.

<br/><br/>
<span class='Readme_header'>Sides Ordered $$ Value</span>
<br/>
Total price of dinners ordered from the Sides category.


<br/><br/>
<span class='Readme_header'>Fast Lane $$ Value</span>
<br/>
Total price of dinners ordered from the Fast Lane category.

<br/><br/>
<span class='Readme_header'>Misc Food $$ Value</span>
<br/>
Total price of all food items sold through the Misc Food Items Entry.

<br/><br/>
<span class='Readme_header'>Misc Non-Food $$ Value</span>
<br/>
Total price of all non-food items sold through the Misc Food Items Entry.

<br/><br/>
<span class='Readme_header'>Menu Items Subtotals</span>
<br/>
Total value (selling price) of items sold plus bonus dinners (promotional items), before markups, rewards and discounts are applied.

<br/><br/>
<span class='Readme_header'>Product Items Subtotal</span>
<br/>
Reserved for future use of allowing the purchase of non-food items through the Dream Dinners web site. This field is related to the Accessories Ordered.

<br/><br/>
<span class='Readme_header'>Home Store Markups</span>
<br/>
Value of markup applied for the session based on items ordered.

<br/><br/>
<span class='Readme_header'>Premium Markups</span>
<br/>
Not applicable after Jan 1, 2007. Represents premium charged for a Quick 6 session.  (Note: You will see values in this column only when reporting prior to 2007 and if your store sold Quick 6 session slots.)

<br/><br/>
<span class='Readme_header'>Volume Reward</span>
<br/>
Total volume reward applied for the session.

<br/><br/>
<span class='Readme_header'>Promo Code Discounts</span>
<br/>
Value of bonus dinners selected as promo items for the session.

<br/><br/>
<span class='Readme_header'>Direct Order Discounts</span>
<br/>
Value of discounts applied through direct order.

<br/><br/>
<span class='Readme_header'>Session Discounts</span>
<br/>
Value of discounts applied when a session with a discount is created (e.g. Private party).

<br/><br/>
<span class='Readme_header'>User Preferred Discounts</span>
<br/>
Value of discounts given to preferred guests attending this session.

<br/><br/>
<span class='Readme_header'>Family Savings Discount</span>
<br/>
Not applicable after August 1, 2007. Represents the value of savings to the guests for when sliding scale pricing applied between August 1, 2006 and August 1, 2007.


<br/><br/>
<span class='Readme_header'>SubTotal</span>
<br/>
Based on all items sold during sessions in the current time period but does not include sales tax if applicable. This value does include all possible discounts
(User Preferred, Session, Family Savings, Direct Order, Promotional Code and Volume Reward Discounts) as well as all possible mark-ups (Premium & Home Store mark-ups).

<br/><br/>
<span class='Readme_header'>Food Taxes</span>
<br/>
Total taxes that may be applicable depending on local regulations.


<br/><br/>
<span class='Readme_header'>Sales Taxes	Total</span>
<br/>
Taxes that may be applicable depending on local regulations.

<br/><br/>
<span class='Readme_header'>Subtotal All Taxes</span>
<br/>
The total of all sales taxes for both food items and products sold for all transactions in the current time period if applicable.
<br/><br/>
<span class='Readme_header'>Grand Total</span>
<br/>
All final sales added together for a given time period. This amount does not include sales adjustments but does include all sales tax if applicable.
