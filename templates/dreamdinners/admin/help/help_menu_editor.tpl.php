			<p><u><strong>Selected Menu</strong></u><a name="selected_menu"></a> (<a href="#topofpage">top</a>)<br />
																				 The &quot;selected menu&quot; function shows you which menu you are currently viewing. This function also allows you to switch between available menus for editing. </p>
			<p><em>Note: In order to save any changes you have made to a specific menu, you must &quot;Finalize&quot; changes before using this function to view another menu. </em></p>
			<p><strong><u>Settings for this menu</u></strong><a name="Settingsforthismenu"></a> (<a href="#topofpage">top</a>)<br />
																								Listed here are the Markup and Volume settings that apply to this specific menu.</p>
			<p><strong>Markup: </strong>3 and 6 serving markup settings for the currently selected menu</p>
			<p><strong>Volume Discount:</strong> The volume discount setting for the currently selected menu</p>
			<p><strong>Save as defaults:</strong> This option allows you to apply the settings for the currently selected menu as a default setting for any unedited menu. </p>
			<p>Example: If the March menu is saved with a 2% markup on 6-serving dinners, and &quot;Save as defaults&quot; is selected, this setting will now apply to other menus that have not yet been given their own specific setting. This will not override custom settings already saved on other menus </p>
			<p><strong><u>Menu Item Details<a name="MenuItemDetails"></a></u></strong> (<a href="#topofpage">top</a>)</p>
			<p><strong>Size: </strong>Represents the serving size of the specific menu item in that row. Side dish items do not currently use this parameter.</p>
			<p><strong>Base Price: </strong>The base price set for an item by the Dream Dinners Home Office</p>
			<p><strong>Current Price:</strong> The current saved price this item will sell for on the customer menu. This includes any options used to increase the price (e.g. Markup, Override, etc.) </p>
			<p><strong>Markup Price:</strong> The price of an item with only markup added. This column is meant to help as reference tool when reviewing the differences between base, markup and override pricing for menu items. </p>
			<p> <strong>Override Price:</strong> Direct input price option for any specific menu item. This value &quot;overrides&quot; base and markup prices. Override Price can be set to any value greater than the base price. </p>
			<p><strong>Preview Price: </strong>The updated price of an item as determined by changes made in the menu editor. Only items that are affected by unsaved changes with have a &quot;Preview&quot; price.</p>
			<p><strong><u>Customizing Fast Lane/Sides <a name="CustomizingFastLaneSides"></a> </u></strong>(<a href="#topofpage">top</a>)</p>
			<p><strong>Fast Lane </strong><br />
				Stores can offer all of the Fast Lane items available for any specific menu or they can choose to &quot;Hide&quot; items from a specific menu. By default all Fast Lane items will be offered on a stores customer facing menu. If Fast Lane items are hidden, they will only be hidden on the customer facing menu (Direct Order menu will not be affected).</p>
			<p>Stores can choose to offer all Fast Lane items, no Fast Lane items or somewhere in between.</p>
			<p><strong>Side Dishes</strong> <br />
				Side Dish options are now controlled by each store. Stores will select from the available list of Side Dishes and select the options appropriate for their store.</p>
			<p>Stores can offer up to 6 specific Side Dish items at a time. Hiding an item does not reduce the number of Side Dishes on a stores menu. Removing an item does reduce the number of Side Dishes on a stores menu.</p>
			<blockquote>
				<p><strong>&quot;Hide&quot; Option Checkbox:</strong> This option allows stores to hide individual Side Dish items from the public customer menus. This does not affect the &quot;Direct Order&quot; process on the BackOffice site.</p>
				<p><strong>&quot;Select a side dish available at your opco&quot;:</strong> This drop down list shows all of the currently available side dishes that can be added to a stores menu. Stores will need to select only the applicable side dishes for there specific store. Stores will need to reference the SUPC numbers shown in parenthesis to determine which side dish items can be offered at their store. </p>
				<p><strong>&quot;Add selected side dish&quot; button:</strong> Adds selected Side Dish from the drop down list to a stores menu. </p>
				<p><strong>&quot;Remove&quot; Option Button</strong> - This button allows stores to remove side dish items from being offered on their store menu. Items will be removed from all stores menus including the direct order menu. </p>
			</blockquote>
			<p><strong><u>KidsChoice</u></strong><a name="KidsChoice"></a> (<a href="#topofpage">top</a>)<br />
																		   KidChoice items are only available through the BackOffice site and do not show on the customer facing menu. Menu markup settings and the override pricing option works the just like standard menu items. </p>
			<p>Note: These items may show as a 6-serv size item on the menu editor, but serving is NOT calculated for these items in customer orders. These items will not contribute to servings based calculations.</p>
			<p><strong><u>In Store Add-ons Editor</u></strong><a name="AddonEditor"></a> (<a href="#topofpage">top</a>)<br />
																						 This item editor will allow you to set  custom pricing options for the Add-on items.</p>
			<p>Things to know about Add-on items</p>
			<ul>
				<li> Since there are so many (&amp; more coming soon) Add-on items they are listed in a drop down list instead of a list on the page.</li>
				<li>You must view/edit details for Add-on items one item at a time.</li>
				<li>To view and/or edit an item
					<ul>
						<li>Select the item from the drop down list</li>
						<li>Click &quot;Load Item in Editor&quot; button</li>
						<li>Make Changes/Updates</li>
						<li>Click &quot;Finalize All Changes&quot; button on the page to save. This MUST be done for every &quot;Add-on&quot; item individual. If you select a different Add-on item before clicking &quot;Finalize All Changes&quot;, you will loose any changes made to that Add-on item.</li>
					</ul>
				</li>
				<li>The &quot;Hide&quot; option for Add-on Items
					<ul>
						<li>This function  helps to customize what is available in the &quot;Add-ons&quot; drop down menu in the Order Edit and Direct Order pages.</li>
						<li>For stores that plan to offer only a select number of Add-on items,this will allow them to prevent certain items from showing in the ordering pages  </li>
						<li>All Add-on  items will always be listed in the Menu Editor drop down menu.</li>
					</ul>
				</li>
			</ul>
			<p><strong><u>Changes not yet saved message</u></strong><a name="Changesnotyetsaved"></a> (<a href="#topofpage">top</a>)<br />
																									  Once you start editing a menu, you will see this reminder message until changes made have been finalized.</p>
			<p><strong><u>Finalize Changes</u></strong><a name="FinalizeChanges"></a> (<a href="#topofpage">top</a>)<br />
																					  Use this button to save changes to the system. The Preview column shows the results of changes made on individual item prices. The &quot;Preview&quot; column price will become the &quot;Current&quot; price, after &quot;Finalize All Changes&quot; is selected.</p>
			<p><strong><u>Reset to Current</u></strong><a name="ResettoCurrent"></a> (<a href="#topofpage">top</a>)<br />
																					 This will reset the currently edited menu back to the last saved/finalized settings.</p>
			<p><strong><u>Business Rules<a name="businessrules"></a> </u></strong>(<a href="#topofpage">top</a>)<br />
																				  Here is some additional information about the menu editor and its functionality. </p>
			<p><strong>Markup changes</strong><br />
				Specifying store menu markup is now much simpler. The whole process can be summed up as follows: </p>
			<ul>
				<li>Beginning in March 2008 each Menu has its own specific markup settings and the markup can be changed at anytime.</li>
				<li>If a default value has been saved, menus that are not assigned markup values will inherit the default values.</li>
				<li>If a &quot;March&quot; specific markup was added in the old markup editor, that value will be initially inherited by the new menu editor and be used until it is overridden with a different value in the new menu editor. </li>
				<li>For a new Mark up to be applied to the March 2008 menu you must visit the new &quot;menu and markup editor&quot; and submit the menu with a markup.</li>
				<li>The old Price Mark up editor is available in the navigation bar as a link labled &quot;Feb Markup Editor&quot;. To change the February 2008 menu markup you must use this &quot;Feb markup editor&quot;.</li>
			</ul>
			<p><strong>Override Pricing</strong></p>
			<ul>
				<li>If an override price is specified for a menu item, that price will always be used regardless of the current markup.</li>
				<li>Override prices must be greater than or equal to the &quot;base price&quot; set by Home Office</li>
			</ul>
			<p><strong>Order Editing</strong><br />
				Once an order is placed, the prices for menu items within the order are based on the prices set at that time. If the prices for these items change at a later date, the prices for this order are not affected.</p>
			<p>If the price of a menu item is changed after an order is placed and the order is then edited,  prices shown in the order editor will  reflect prices that were in effect at the time the order was placed. This is only true for menu items that have already been ordered. Items not in the original order will use current prices. This rule is in place regardless of how the price changed: by price override or change in markup.</p>
			<p><strong>Preferred User discount</strong><br />
				The Side Dishes are now exempted from the Preferred User Discount. </p>
			<ul>
				<li>Flat Discount &ndash; The rate is multiplied by the number of items (except sides) and then the sides total is added back into the subtotal.</li>
				<li>Percent Discount &ndash; The Discount is applied to the discounted subtotal as before however the sides total is now subtracted from discounted subtotal before the discount is calculated.</li>
			</ul>
			<p><strong>Promotions</strong> (aka: promos, free meals) <br />
										   Promotions cannot currently be associated with Side Dishes.</p>
			<p><strong>Reports</strong><br />
				Reports will list all menu items used in the current menu. Items that have been hidden or removed will still be listed in the Entr&eacute;e and Session reports.</p>
			<p><strong>Side Dishes</strong></p>
			<ul>
				<li>Up to 6 Side Dishes can be added to a store specific menu.</li>
				<li>The SUPC code is listed to aid in finding the sides that are available at your stores OpCo.</li>
			</ul>
			<p><strong>Item hiding</strong> </p>
			<ul>
				<li>Specific Fast Lane and Side Dish items can be hidden from customer facing menus.</li>
				<li>Hiding an item has no effect on previously placed orders.</li>
				<li> Hiding an &quot;Add-on&quot; items will hide the item from drop down menu in Direct Order &amp; Order Edit.
					<ul>
						<li> The is simply a function that helps to customize what is listed in the drop down menu for ordering.</li>
						<li>The items will always be listed in the Menu Editor drop down menu.</li>
					</ul>
				</li>
			</ul>
